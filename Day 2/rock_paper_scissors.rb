def points(shape)
  points = 0
  if shape == "Rock" 
    points += 1
  elsif shape == "Paper" 
    points += 2
  elsif shape == "Scissors" 
    points += 3
  end
return points
end

def is_win(our_shape, opponent_shape)
  if (our_shape == "Rock") && (opponent_shape == "Scissors")
    return true
  elsif (our_shape == "Paper") && (opponent_shape == "Rock")
    return true
  elsif (our_shape == "Scissors") && (opponent_shape == "Paper")
    return true
  else
    return false
  end
end

def is_draw(our_shape, opponent_shape)
  if our_shape == opponent_shape
    return true
  end
end

